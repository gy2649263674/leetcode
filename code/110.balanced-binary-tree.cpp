/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution
{
public:
    int getD(TreeNode *r)
    {
        return r == NULL ? 0 : max(getD(r->left), getD(r->right)) + 1;
    }
    int solve(TreeNode *r)
    {
        if (r == nullptr)
        {
            return 0;
        }
        int ld = solve(r->left);
        int rd = solve(r->right);
        return abs(ld - rd) <= 1 && rd != -1 && ld != -1 ? max(ld, rd) + 1 : -1;
    }
    bool isBalanced(TreeNode *root)
    {
        return solve(root) != -1;
        // use the ch ild info to make self's ans
        return root == NULL || abs(getD(root->left) - getD(root->right)) <= 1 && isBalanced(root->left) && isBalanced(root->right);
    }
};
